/** 텍스트에서 금칙어 감지 — 매칭된 단어 목록 반환 (없으면 빈 배열) */
export function detectBannedWords(text: string): string[] {
  if (!text) return [];
  const normalized = text.replace(/\s+/g, '');
  return BANNED_WORDS.filter((word) => normalized.includes(word));
}

/** 금칙어 포함 여부만 boolean으로 반환 */
export function hasBannedWords(text: string): boolean {
  return detectBannedWords(text).length > 0;
}
